/* 
    安装 yarn 工具
    npm install -g yarn

    1. 初始化包
    npm init
    yarn init
    2. 安装包
    npm install xxx --save
    yarn add xxx (会自动加上--save)
    3. 全局安装
    npm install -g xxx
    yarn global add xxx
    4. 安装所有依赖
    npm install
    yarn install
    5. 安装开发依赖的包
    npm install xxx --save-dev
    yarn add xxx -dev
    6. 移除包
    npm uninstall xxx
    yarn remove xxx
    7. 更新包
    npm update xxx
    yarn upgrade xxx
    8. 执行包
    npm run 
    yarn run 
    9. 设置下载镜像的地址
    npm config set registry url
    yarn config set registry url
    
*/